    public void EnregistrementNewClasse() {

        try {
            int id_class=retourneMaxNumber()+1;
            String nom_class = jTextField1.getText();//
            String prof_princi = jTextField2.getText();//
            String capacite1 = jTextField3.getText();//
            int capacite=Integer.parseInt(capacite1);
            if ((nom_class.isEmpty())||(capacite==0)) {
                JOptionPane.showConfirmDialog(null, "SAISIR LE NOM DE LA CLASSE ET LA CAPACITE\n LA CAPACITE DOIT ETRE UN ENTIER",
                        "SELECTION NULLE", JOptionPane.OK_CANCEL_OPTION);
            } else {
                Connector1.statement.executeUpdate("INSERT INTO classe(id_class,nom_class,prof_princi,capacite) values('" + id_class + "','" + nom_class + "','" + prof_princi + "','" + capacite + "')");

                int reponse = JOptionPane.showConfirmDialog(null, "ENREGISTREMENT EFFECTUE AVEC SUCCES\n"
                        + "    UNE NOUVELLE CLASSE?", " Alors cliquer sur oui", JOptionPane.YES_NO_OPTION);
                if (reponse == JOptionPane.YES_OPTION) {
                    jTextField1.setText(null);
                    jTextField2.setText(null);
                    jTextField3.setText(null);

                } else {
                    dispose();
                }
            }



        } catch (Exception ex) {
                    JOptionPane.showConfirmDialog(null, "SAISIR LE NOM DE LA CLASSE ET LA CAPACITE\n LA CAPACITE DOIT ETRE UN ENTIER",
                        "SELECTION NULLE", JOptionPane.OK_CANCEL_OPTION);
           JOptionPane.showMessageDialog(null, "Erreur :" + ex.getMessage());
            //dispose();
        }
    }

